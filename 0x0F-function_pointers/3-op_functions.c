#include "3-calc.h"

/**
 * op_add - retuns sum of two numbers
 * @a: first number
 * @b: second number
 * Return: the sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - retruns difference of two numbers
 * @a: first number
 * @b: second number
 * Return: the difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns multiplication of two numbers
 * @a: first number
 * @b: second number
 * Return: the product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns division of two numbers
 * @a: first number
 * @b: second number
 * Return: The quotient of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns remainder in division
 * @a: first number
 * @b: second number
 * Return: the remainder of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
